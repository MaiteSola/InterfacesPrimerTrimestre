
export function sumar(num1: number, num2: number){

  return num1 + num2;
}

export function restar(num1: number, num2: number){

  return num1 - num2;
}

export function multiplicar(num1: number, num2: number){

  return num1 * num2;
}

export function dividir(num1: number, num2: number){

  return num1 / num2;
}
